class Dog():
    
    def __init__(self, name):
        self.name = name 
        
    def speak(self):
        return self.name + "says Woof!"
    
class Cat():
    def __init__(self, name):
        self.name = name
        
    def speak():
        return self.name + "say Meow!"
    
